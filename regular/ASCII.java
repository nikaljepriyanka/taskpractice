package regular;

public class ASCII {
	//American Standard Code for Information Interchange
			//value of 'a'=97  & value of 'z'=122
			//value of 'A'=65  & value of 'Z'=90
			
			char g='s';
			int b=g;
			
			char jay='U';
			int t=jay;
			
	public static void main(String[] args) {
		ASCII o=new ASCII();
		System.out.println(o.b);
		System.out.println(o.t);
	}

}
