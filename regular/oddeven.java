package regular;

public class oddeven {

	public static void main(String[] args) {
		//odd number 
		for(int a=1;a<=50;a++) {
			if(a%2!=0) {
				System.out.println(a);
					
			}
		}
		//even number
		for(int b=1;b<=50;b++) {
			if(b%2==0) {
				System.out.println(b);
				
			}
			
		}
		
		
	}

}
